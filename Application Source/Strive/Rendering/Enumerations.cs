using System;

namespace Strive.Rendering
{

	/// <summary>
	/// Possible render targets
	/// </summary>
	/// <remarks></remarks>
	public enum RenderTarget
	{
		/// <summary>
		/// Unspecified
		/// </summary>
		Unspecified,
		/// <summary>
		/// Renders to the window
		/// </summary>
		Window,
		/// <summary>
		/// Renders to the entire screen
		/// </summary>
		FullScreen,
		/// <summary>
		/// Renders to a picture box
		/// </summary>
		PictureBox

	}

}
