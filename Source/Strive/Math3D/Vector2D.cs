using System;

namespace Strive.Math3D
{
	/// <summary>
	/// Summary description for Vector2d.
	/// </summary>
	public class Vector2D
	{
		public float X;
		public float Y;

		public Vector2D() {
			this.X = 0;
			this.Y = 0;
		}

		public Vector2D( float x, float y ) {
			X = x;
			Y = y;
		}
	}
}
